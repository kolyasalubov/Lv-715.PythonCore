def get_sum(number_1: int, number_2: int) -> int:
    """This function accepts two numbers and returns sum of them"""

    sum = number_1 + number_2

    return sum

def get_diffirence(number_1: int, number_2: int) -> int:
    """This function accepts two numbers and returns differens between them"""

    diff = number_1 - number_2

    return diff

def get_product(number_1: int, number_2: int) -> int:
    """This function accepts two numbers and returns product of them"""

    number_product = number_1 * number_2

    return number_product

def get_division(number_1: int, number_2: int) -> int:
    """This function accepts two numbers and returns division of them"""

    number_division = number_1 / number_2

    return number_division