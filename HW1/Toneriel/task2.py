variable_a = int(input('Enter variable a '))
variable_b = int(input('And enter variable b '))

addition = variable_a + variable_b
subtraction = variable_a - variable_b
multiplication = variable_a * variable_b
division = variable_a / variable_b
exponentiation = variable_a ** variable_b

print(f'{variable_a} + {variable_b} = {addition}')
print(f'{variable_a} - {variable_b} = {subtraction}')
print(f'{variable_a} * {variable_b} = {multiplication}')
print(f'{variable_a} / {variable_b} = {division}')
print (f'{variable_a} ** {variable_b} = {exponentiation}')
